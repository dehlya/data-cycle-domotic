def check_python():
    v = sys.version_info
    if (v.major, v.minor) < (3, 11):
        die(f"Python 3.11+ required, you have {v.major}.{v.minor}",
            "Install from https://www.python.org/downloads/")
    ok(f"Python {v.major}.{v.minor}.{v.micro}")


def check_cmd(cmd, hint=None):
    if shutil.which(cmd) is None:
        die(f"`{cmd}` not found in PATH", hint)
    ok(f"`{cmd}` available")


def parse_db_url(db_url: str):
    """Return (host, port, user, password, dbname) from a postgres URL."""
    import urllib.parse as up
    p = up.urlparse(db_url)
    return (
        p.hostname or "localhost",
        p.port or 5432,
        up.unquote(p.username or ""),
        up.unquote(p.password or ""),
        (p.path or "/").lstrip("/") or "postgres",
    )


def run_python_snippet(venv: Path, snippet: str) -> tuple[int, str, str]:
    """Run a one-line python snippet in the venv, return (code, stdout, stderr)."""
    py = venv_python(venv)
    res = subprocess.run([str(py), "-c", snippet], capture_output=True, text=True)
    return res.returncode, res.stdout.strip(), res.stderr.strip()


def validate_postgres_admin(venv: Path) -> bool:
    """Confirm we can reach Postgres as admin."""
    snippet = (
        "import sys\n"
        "try:\n"
        "    import psycopg2\n"
        "except ImportError:\n"
        "    sys.exit('NO_DRIVER')\n"
        "try:\n"
        f"    c = psycopg2.connect(host='{PG_HOST}', port={PG_PORT}, user='{PG_ADMIN_USER}', password='''{PG_ADMIN_PASSWORD}''', dbname='postgres', connect_timeout=5)\n"
        "    c.close()\n"
        "    print('OK')\n"
        "except Exception as e:\n"
        "    sys.exit('CONN_FAIL:' + str(e))\n"
    )
    rc, out, err = run_python_snippet(venv, snippet)
    if rc == 0:
        ok(f"PostgreSQL reachable at {PG_HOST}:{PG_PORT} (as {PG_ADMIN_USER})")
        return True
    fail(f"Cannot connect to Postgres as {PG_ADMIN_USER}", hint=err.splitlines()[0] if err else "")
    return False


def ensure_db_and_user(venv: Path) -> bool:
    """
    Using admin creds:
      1. Create app user if missing (with given password)
      2. Create target DB if missing (owned by app user)
      3. Grant all privileges on DB + public schema to app user
    """
    snippet = f"""
import sys, psycopg2
from psycopg2 import sql

ADMIN_HOST = {PG_HOST!r}
ADMIN_PORT = {PG_PORT!r}
ADMIN_USER = {PG_ADMIN_USER!r}
ADMIN_PWD  = {PG_ADMIN_PASSWORD!r}
APP_DB     = {PG_DATABASE!r}
APP_USER   = {PG_APP_USER!r}
APP_PWD    = {PG_APP_PASSWORD!r}

def connect(dbname):
    return psycopg2.connect(host=ADMIN_HOST, port=int(ADMIN_PORT),
                            user=ADMIN_USER, password=ADMIN_PWD,
                            dbname=dbname, connect_timeout=5)

try:
    c = connect('postgres'); c.autocommit = True; cur = c.cursor()

    # 1. user
    cur.execute("SELECT 1 FROM pg_roles WHERE rolname = %s", (APP_USER,))
    if cur.fetchone() is None:
        cur.execute(sql.SQL("CREATE ROLE {{}} WITH LOGIN PASSWORD %s").format(sql.Identifier(APP_USER)), (APP_PWD,))
        print('USER_CREATED')
    else:
        cur.execute(sql.SQL("ALTER ROLE {{}} WITH LOGIN PASSWORD %s").format(sql.Identifier(APP_USER)), (APP_PWD,))
        print('USER_EXISTS')

    # 2. database
    cur.execute("SELECT 1 FROM pg_database WHERE datname = %s", (APP_DB,))
    if cur.fetchone() is None:
        cur.execute(sql.SQL("CREATE DATABASE {{}} OWNER {{}}").format(sql.Identifier(APP_DB), sql.Identifier(APP_USER)))
        print('DB_CREATED')
    else:
        print('DB_EXISTS')

    # 3. grants at DB level
    cur.execute(sql.SQL("GRANT ALL PRIVILEGES ON DATABASE {{}} TO {{}}").format(sql.Identifier(APP_DB), sql.Identifier(APP_USER)))
    cur.close(); c.close()

    # 4. grants inside the DB (public schema access for create + default schema perms)
    c = connect(APP_DB); c.autocommit = True; cur = c.cursor()
    cur.execute(sql.SQL("GRANT ALL ON SCHEMA public TO {{}}").format(sql.Identifier(APP_USER)))
    cur.close(); c.close()

    print('DONE')
except Exception as e:
    sys.exit('FAIL:' + str(e))
"""
    rc, out, err = run_python_snippet(venv, snippet)
    if rc != 0:
        fail("Could not set up DB + app user as admin", err.splitlines()[0] if err else "")
        return False
    lines = out.splitlines()
    if "USER_CREATED" in lines:
        ok(f"App user '{PG_APP_USER}' created")
    elif "USER_EXISTS" in lines:
        ok(f"App user '{PG_APP_USER}' already exists (password reset)")
    if "DB_CREATED" in lines:
        ok(f"Database '{PG_DATABASE}' created (owner: {PG_APP_USER})")
    elif "DB_EXISTS" in lines:
        ok(f"Database '{PG_DATABASE}' already exists")
    ok(f"Privileges granted on '{PG_DATABASE}' to '{PG_APP_USER}'")
    return True


def validate_postgres_app(venv: Path, db_url: str) -> bool:
    """Confirm the app user can now connect to the target DB."""
    host, port, user, password, dbname = parse_db_url(db_url)
    snippet = (
        "import sys, psycopg2\n"
        "try:\n"
        f"    c = psycopg2.connect(host='{host}', port={port}, user='{user}', password='''{password}''', dbname='{dbname}', connect_timeout=5)\n"
        "    c.close()\n"
        "    print('OK')\n"
        "except Exception as e:\n"
        "    sys.exit('FAIL:' + str(e))\n"
    )
    rc, out, err = run_python_snippet(venv, snippet)
    if rc == 0:
        ok(f"App user '{user}' can connect to '{dbname}'")
        return True
    fail(f"App user can't connect to '{dbname}'", err.splitlines()[0] if err else "")
    return False


def validate_mysql(venv: Path, mysql_url: str) -> bool:
    snippet = (
        "import sys\n"
        "try:\n"
        "    import pymysql\n"
        "except ImportError:\n"
        "    sys.exit('NO_DRIVER')\n"
        "from urllib.parse import urlparse, unquote\n"
        f"u = urlparse('{mysql_url}')\n"
        "try:\n"
        "    c = pymysql.connect(host=u.hostname, port=u.port or 3306, user=unquote(u.username or ''), password=unquote(u.password or ''), database=(u.path or '/').lstrip('/'), connect_timeout=5)\n"
        "    c.close()\n"
        "    print('OK')\n"
        "except Exception as e:\n"
        "    sys.exit('FAIL:' + str(e)[:200])\n"
    )
    rc, out, err = run_python_snippet(venv, snippet)
    if rc == 0:
        ok("MySQL source reachable")
        return True
    warn(f"MySQL source unreachable -- sensor DB syncing will fail")
    if err:
        warn(f"  details: {err.splitlines()[0]}")
    return False


def validate_sftp(venv: Path, host: str, port: str, user: str, password: str) -> bool:
    if not (host and user and password):
        SUMMARY.add_skipped("sFTP check", "credentials not provided")
        return True  # sFTP is optional
    snippet = (
        "import sys\n"
        "try:\n"
        "    import paramiko\n"
        "except ImportError:\n"
        "    sys.exit('NO_DRIVER')\n"
        "try:\n"
        f"    t = paramiko.Transport(('{host}', {port or 22}))\n"
        "    t.banner_timeout = 10\n"
        f"    t.connect(username='{user}', password='{password}')\n"
        "    t.close()\n"
        "    print('OK')\n"
        "except Exception as e:\n"
        "    sys.exit('FAIL:' + str(e)[:200])\n"
    )
    rc, out, err = run_python_snippet(venv, snippet)
    if rc == 0:
        ok(f"sFTP reachable at {host}:{port or 22}")
        return True
    warn(f"sFTP unreachable at {host}:{port or 22}")
    if err:
        warn(f"  details: {err.splitlines()[0]}")
    return False


def mount_smb_share():
    """Mount the SMB share as a drive on Windows, or skip on other OSes."""
    if not (SMB_SHARE and SMB_USER and SMB_PASSWORD and SMB_DRIVE):
        SUMMARY.add_skipped("SMB mount", "credentials not provided")
        return True

    if os.name != "nt":
        warn(f"Non-Windows: skipping auto-mount. Mount {SMB_SHARE} at {SMB_DRIVE} manually (mount.cifs) before running the pipeline.")
        return True

    # Check if already mounted
    drive_letter = SMB_DRIVE.rstrip("\\").rstrip(":")
    existing = Path(drive_letter + ":\\")
    if existing.exists():
        ok(f"{SMB_DRIVE} already appears mounted")
        return True

    # net use Z: \\server\share /USER:user password /PERSISTENT:YES
    try:
        subprocess.run(
            ["net", "use", SMB_DRIVE, SMB_SHARE,
             f"/USER:{SMB_USER}", SMB_PASSWORD,
             "/PERSISTENT:YES"],
            check=True,
            capture_output=True,
            text=True,
        )
        ok(f"Mounted {SMB_SHARE} -> {SMB_DRIVE}")
        return True
    except subprocess.CalledProcessError as e:
        stderr = (e.stderr or "").strip().splitlines()
        hint = stderr[0] if stderr else ""
        warn(f"Could not mount SMB share: {hint}")
        warn(f"  You can retry manually: net use {SMB_DRIVE} {SMB_SHARE} /USER:{SMB_USER} <password>")
        return False


def clone_repo(install_dir: Path):
    # Fresh clone
    if not install_dir.exists() or not any(install_dir.iterdir()):
        install_dir.parent.mkdir(parents=True, exist_ok=True)
        subprocess.run(
            ["git", "clone", "--branch", REPO_BRANCH, "--depth", "1", REPO_URL, str(install_dir)],
            check=True
        )
        ok(f"Repo cloned to {install_dir}")
        return

    # Existing dir — try to update it if it's the right repo
    if not (install_dir / ".git").exists():
        warn(f"{install_dir} already exists but isn't a git repo -- skipping update")
        return

    try:
        # Verify it's our repo
        res = subprocess.run(["git", "-C", str(install_dir), "remote", "get-url", "origin"],
                             capture_output=True, text=True, check=True)
        remote = res.stdout.strip()
        # Be tolerant of ssh vs https form
        if "data-cycle-domotic" not in remote:
            warn(f"{install_dir} is a git repo but points to {remote} -- skipping update")
            return

        subprocess.run(["git", "-C", str(install_dir), "fetch", "--depth", "1", "origin", REPO_BRANCH],
                       check=True, capture_output=True)
        subprocess.run(["git", "-C", str(install_dir), "checkout", REPO_BRANCH],
                       check=True, capture_output=True)
        subprocess.run(["git", "-C", str(install_dir), "reset", "--hard", f"origin/{REPO_BRANCH}"],
                       check=True, capture_output=True)
        ok(f"Repo updated to latest origin/{REPO_BRANCH}")
    except subprocess.CalledProcessError as e:
        warn(f"Could not update existing repo: {(e.stderr or b'').decode(errors='ignore').strip() or e}")
        warn("Delete the folder and rerun for a clean install.")


def write_env(install_dir: Path):
    env_path = install_dir / ".env"
    env_path.write_text(ENV_CONFIG, encoding="utf-8")
    ok(f".env written ({len(ENV_CONFIG)} chars)")


def create_venv(install_dir: Path) -> Path:
    venv = install_dir / ".venv"
    if venv.exists():
        warn(".venv already exists -- reusing")
    else:
        subprocess.run([sys.executable, "-m", "venv", str(venv)], check=True)
        ok("Virtual environment created")
    return venv


def venv_python(venv: Path) -> Path:
    if os.name == "nt":
        return venv / "Scripts" / "python.exe"
    return venv / "bin" / "python"


def install_deps(venv: Path, install_dir: Path):
    py = venv_python(venv)
    req = install_dir / "requirements.txt"
    if not req.exists():
        die(f"requirements.txt not found at {req}")
    subprocess.run([str(py), "-m", "pip", "install", "--upgrade", "pip", "--quiet"], check=True)
    subprocess.run([str(py), "-m", "pip", "install", "-r", str(req), "--quiet"], check=True)
    ok("Python dependencies installed")


def run_script(venv: Path, install_dir: Path, *args, label=None):
    py = venv_python(venv)
    env = os.environ.copy()
    env["PYTHONPATH"] = str(install_dir)
    try:
        subprocess.run([str(py), *args], cwd=str(install_dir), env=env, check=True)
        if label:
            ok(label)
    except subprocess.CalledProcessError as e:
        fail(f"{label or args} failed (exit {e.returncode})")
        raise


def create_schemas(venv: Path, install_dir: Path):
    run_script(venv, install_dir, "-m", "etl.bronze_to_silver.create_silver", label="Silver schema created")
    run_script(venv, install_dir, "-m", "etl.silver_to_gold.create_gold",     label="Gold schema created")


def run_initial_etl(venv: Path, install_dir: Path):
    run_script(venv, install_dir, "-m", "etl.silver_to_gold.populate_gold", label="Initial gold ETL complete")


def verify_data(venv: Path, db_url: str):
    """Quick sanity check: count rows in key gold tables."""
    host, port, user, password, dbname = parse_db_url(db_url)
    snippet = (
        "import psycopg2, sys\n"
        f"c = psycopg2.connect(host='{host}', port={port}, user='{user}', password='{password}', dbname='{dbname}', connect_timeout=5)\n"
        "cur = c.cursor()\n"
        "for t in ['dim_apartment','dim_room','dim_device','fact_environment_minute','fact_energy_minute','fact_presence_minute']:\n"
        "    try:\n"
        "        cur.execute(f'SELECT COUNT(*) FROM gold.{t}')\n"
        "        print(f'{t}\\t{cur.fetchone()[0]}')\n"
        "    except Exception as e:\n"
        "        print(f'{t}\\tERR: ' + str(e)[:60])\n"
        "cur.close(); c.close()\n"
    )
    rc, out, err = run_python_snippet(venv, snippet)
    if rc != 0:
        warn(f"Verification query failed: {err or out}")
        return
    for line in out.splitlines():
        parts = line.split("\t")
        if len(parts) == 2:
            t, n = parts
            if n.startswith("ERR"):
                fail(f"  gold.{t} — {n}")
            elif int(n) == 0:
                warn(f"  gold.{t} — empty")
            else:
                ok(f"  gold.{t} — {int(n):,} rows")


def maybe_start_watcher(venv: Path, install_dir: Path):
    if not sys.stdin.isatty():
        return
    if not ask_yes_no("Start the watcher now? (runs continuously in this terminal)", default=False):
        return
    print()
    print(f"{DIM}Starting watcher... Ctrl+C to stop.{RESET}")
    py = venv_python(venv)
    env = os.environ.copy()
    env["PYTHONPATH"] = str(install_dir)
    try:
        subprocess.run([str(py), "ingestion/fast_flow/watcher.py"], cwd=str(install_dir), env=env)
    except KeyboardInterrupt:
        print(f"\n{DIM}Watcher stopped.{RESET}")


